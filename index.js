function add(a, b) {
  return a + b
}

function subtract(a, b) {
  return a - b
}

function multiply(a, b) {
  return a * b
}

function divide(a, b) {
  return a / b
}

function inc(n) {
  var a = n + 1
  return a
}

function dec(n) {
  var b = n - 1
  return b
}

